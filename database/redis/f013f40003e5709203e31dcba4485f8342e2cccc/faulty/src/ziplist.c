unsigned int ziplistCompare(unsigned char *p, unsigned char *sstr, unsigned int slen) {
    zlentry entry;
    unsigned char sencoding;
    long long zval, sval;
    if (p[0] == 255) return 0;

    entry = zipEntry(p);
    if ((((entry.encoding) & 0xc0) < 0xc0)) {

        if (entry.len == slen) {
            return memcmp(p+entry.headersize,sstr,slen) == 0;
        } else {
            return 0;
        }
    } else {

        if (zipTryEncoding(sstr,slen,&sval,&sencoding)) {
            if (entry.encoding == sencoding) {
                zval = zipLoadInteger(p+entry.headersize,entry.encoding);
                return zval == sval;
            }
        }
    }
    return 0;
}


unsigned int ziplistLen(unsigned char *zl) {
    unsigned int len = 0;
    if ((*((uint16_t*)((zl)+sizeof(uint32_t)*2))) < (65535)) {
        len = (*((uint16_t*)((zl)+sizeof(uint32_t)*2)));
    } else {
        unsigned char *p = zl+(sizeof(uint32_t)*2+sizeof(uint16_t));
        while (*p != 255) {
            p += zipRawEntryLength(p);
            len++;
        }


        if (len < (65535)) (*((uint16_t*)((zl)+sizeof(uint32_t)*2))) = len;
    }
    return len;
}


