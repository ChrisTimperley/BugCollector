void sendReplyToClient(aeEventLoop *el, int fd, void *privdata, int mask) {
    redisClient *c = privdata;
    int nwritten = 0, totwritten = 0, objlen;
    robj *o;
    ((void) el);
    ((void) mask);

    while(c->bufpos > 0 || ((c->reply)->len)) {
        if (c->bufpos > 0) {
            if (c->flags & 2) {

                nwritten = c->bufpos - c->sentlen;
            } else {
                nwritten = write(fd,c->buf+c->sentlen,c->bufpos-c->sentlen);
                if (nwritten <= 0) break;
            }
            c->sentlen += nwritten;
            totwritten += nwritten;



            if (c->sentlen == c->bufpos) {
                c->bufpos = 0;
                c->sentlen = 0;
            }
        } else {
            o = ((((c->reply)->head))->value);
            objlen = sdslen(o->ptr);

            if (objlen == 0) {
                listDelNode(c->reply,((c->reply)->head));
                continue;
            }

            if (c->flags & 2) {

                nwritten = objlen - c->sentlen;
            } else {
                nwritten = write(fd, ((char*)o->ptr)+c->sentlen,objlen-c->sentlen);
                if (nwritten <= 0) break;
            }
            c->sentlen += nwritten;
            totwritten += nwritten;


            if (c->sentlen == objlen) {
                listDelNode(c->reply,((c->reply)->head));
                c->sentlen = 0;
                c->reply_bytes -= objlen;
            }
        }





        if (totwritten > (1024*64)) break;
    }
    if (nwritten == -1) {
        if ((*__errno_location ()) == 11) {
            nwritten = 0;
        } else {
            redisLog(1,
                "Error writing to client: %s", strerror((*__errno_location ())));
            freeClient(c);
            return;
        }
    }
    if (totwritten > 0) c->lastinteraction = time(((void *)0));
    if (c->bufpos == 0 && ((c->reply)->len) == 0) {
        c->sentlen = 0;
        aeDeleteFileEvent(server.el,c->fd,2);


        if (c->flags & 128) freeClient(c);
    }
}


