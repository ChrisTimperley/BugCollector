int processInlineBuffer(redisClient *c) {
    char *newline = strstr(c->querybuf,"\r\n");
    int argc, j;
    sds *argv;
    size_t querylen;


    if (newline == ((void *)0)) {
        if (sdslen(c->querybuf) > (1024*64)) {
            addReplyError(c,"Protocol error: too big inline request");
            setProtocolError(c,0);
        }
        return -1;
    }


    querylen = newline-(c->querybuf);
    argv = sdssplitlen(c->querybuf,querylen," ",1,&argc);


    c->querybuf = sdsrange(c->querybuf,querylen+2,-1);


    if (c->argv) zfree(c->argv);
    c->argv = zmalloc(sizeof(robj*)*argc);


    for (c->argc = 0, j = 0; j < argc; j++) {
        if (sdslen(argv[j])) {
            c->argv[c->argc] = createObject(0,argv[j]);
            c->argc++;
        } else {
            sdsfree(argv[j]);
        }
    }
    zfree(argv);
    return 0;
}



static void setProtocolError(redisClient *c, int pos) {
    if (server.verbosity >= 1) {
        sds client = getClientInfoString(c);
        redisLog(1,
            "Protocol error from client: %s", client);
        sdsfree(client);
    }
    c->flags |= 128;
    c->querybuf = sdsrange(c->querybuf,pos,-1);
}

int processMultibulkBuffer(redisClient *c) {
    char *newline = ((void *)0);
    int pos = 0, ok;
    long long ll;

    if (c->multibulklen == 0) {

        ((c->argc == 0)?(void)0 : (_redisAssertWithInfo(c,((void *)0),"c->argc == 0","networking.c",801),_exit(1)));


        newline = strchr(c->querybuf,'\r');
        if (newline == ((void *)0)) {
            if (sdslen(c->querybuf) > (1024*64)) {
                addReplyError(c,"Protocol error: too big mbulk count string");
                setProtocolError(c,0);
            }
            return -1;
        }


        if (newline-(c->querybuf) > ((signed)sdslen(c->querybuf)-2))
            return -1;



        ((c->querybuf[0] == '*')?(void)0 : (_redisAssertWithInfo(c,((void *)0),"c->querybuf[0] == '*'","networking.c",819),_exit(1)));
        ok = string2ll(c->querybuf+1,newline-(c->querybuf+1),&ll);
        if (!ok || ll > 1024*1024) {
            addReplyError(c,"Protocol error: invalid multibulk length");
            setProtocolError(c,pos);
            return -1;
        }

        pos = (newline-c->querybuf)+2;
        if (ll <= 0) {
            c->querybuf = sdsrange(c->querybuf,pos,-1);
            return 0;
        }

        c->multibulklen = ll;


        if (c->argv) zfree(c->argv);
        c->argv = zmalloc(sizeof(robj*)*c->multibulklen);
    }

    ((c->multibulklen > 0)?(void)0 : (_redisAssertWithInfo(c,((void *)0),"c->multibulklen > 0","networking.c",840),_exit(1)));
    while(c->multibulklen) {

        if (c->bulklen == -1) {
            newline = strchr(c->querybuf+pos,'\r');
            if (newline == ((void *)0)) {
                if (sdslen(c->querybuf) > (1024*64)) {
                    addReplyError(c,"Protocol error: too big bulk count string");
                    setProtocolError(c,0);
                }
                break;
            }


            if (newline-(c->querybuf) > ((signed)sdslen(c->querybuf)-2))
                break;

            if (c->querybuf[pos] != '$') {
                addReplyErrorFormat(c,
                    "Protocol error: expected '$', got '%c'",
                    c->querybuf[pos]);
                setProtocolError(c,pos);
                return -1;
            }

            ok = string2ll(c->querybuf+pos+1,newline-(c->querybuf+pos+1),&ll);
            if (!ok || ll < 0 || ll > 512*1024*1024) {
                addReplyError(c,"Protocol error: invalid bulk length");
                setProtocolError(c,pos);
                return -1;
            }

            pos += newline-(c->querybuf+pos)+2;
            if (ll >= (1024*32)) {




                c->querybuf = sdsrange(c->querybuf,pos,-1);
                pos = 0;


                c->querybuf = sdsMakeRoomFor(c->querybuf,ll+2);
            }
            c->bulklen = ll;
        }


        if (sdslen(c->querybuf)-pos < (unsigned)(c->bulklen+2)) {

            break;
        } else {



            if (pos == 0 &&
                c->bulklen >= (1024*32) &&
                (signed) sdslen(c->querybuf) == c->bulklen+2)
            {
                c->argv[c->argc++] = createObject(0,c->querybuf);
                sdsIncrLen(c->querybuf,-2);
                c->querybuf = sdsempty();


                c->querybuf = sdsMakeRoomFor(c->querybuf,c->bulklen+2);
                pos = 0;
            } else {
                c->argv[c->argc++] =
                    createStringObject(c->querybuf+pos,c->bulklen);
                pos += c->bulklen+2;
            }
            c->bulklen = -1;
            c->multibulklen--;
        }
    }


    if (pos) c->querybuf = sdsrange(c->querybuf,pos,-1);


    if (c->multibulklen == 0) return 0;


    return -1;
}

void processInputBuffer(redisClient *c) {

    while(sdslen(c->querybuf)) {

        if (c->flags & 16) return;




        if (c->flags & 128) return;


        if (!c->reqtype) {
            if (c->querybuf[0] == '*') {
                c->reqtype = 2;
            } else {
                c->reqtype = 1;
            }
        }

        if (c->reqtype == 1) {
            if (processInlineBuffer(c) != 0) break;
        } else if (c->reqtype == 2) {
            if (processMultibulkBuffer(c) != 0) break;
        } else {
            _redisPanic("\"Unknown request type\"","networking.c",951),_exit(1);
        }


        if (c->argc == 0) {
            resetClient(c);
        } else {

            if (processCommand(c) == 0)
                resetClient(c);
        }
    }
}

void readQueryFromClient(aeEventLoop *el, int fd, void *privdata, int mask) {
    redisClient *c = (redisClient*) privdata;
    int nread, readlen;
    size_t qblen;
    ((void) el);
    ((void) mask);

    server.current_client = c;
    readlen = (1024*16);






    if (c->reqtype == 2 && c->multibulklen && c->bulklen != -1
        && c->bulklen >= (1024*32))
    {
        int remaining = (unsigned)(c->bulklen+2)-sdslen(c->querybuf);

        if (remaining < readlen) readlen = remaining;
    }

    qblen = sdslen(c->querybuf);
    c->querybuf = sdsMakeRoomFor(c->querybuf, readlen);
    nread = read(fd, c->querybuf+qblen, readlen);
    if (nread == -1) {
        if ((*__errno_location ()) == 11) {
            nread = 0;
        } else {
            redisLog(1, "Reading from client: %s",strerror((*__errno_location ())));
            freeClient(c);
            return;
        }
    } else if (nread == 0) {
        redisLog(1, "Client closed connection");
        freeClient(c);
        return;
    }
    if (nread) {
        sdsIncrLen(c->querybuf,nread);
        c->lastinteraction = time(((void *)0));
    } else {
        server.current_client = ((void *)0);
        return;
    }
    if (sdslen(c->querybuf) > server.client_max_querybuf_len) {
        sds ci = getClientInfoString(c), bytes = sdsempty();

        bytes = sdscatrepr(bytes,c->querybuf,64);
        redisLog(3,"Closing client that reached max query buffer length: %s (qbuf initial bytes: %s)", ci, bytes);
        sdsfree(ci);
        sdsfree(bytes);
        freeClient(c);
        return;
    }
    processInputBuffer(c);
    server.current_client = ((void *)0);
}

