int _dictClear(dict *d, dictht *ht)
{
    unsigned long i;


    for (i = 0; i < ht->size && ht->used > 0; i++) {
        dictEntry *he, *nextHe;

        if ((he = ht->table[i]) == ((void *)0)) continue;
        while(he) {
            nextHe = he->next;
            if ((d)->type->keyDestructor) (d)->type->keyDestructor((d)->privdata, (he)->key);
            if ((d)->type->valDestructor) (d)->type->valDestructor((d)->privdata, (he)->val);
            zfree(he);
            ht->used--;
            he = nextHe;
        }
    }

    zfree(ht->table);

    _dictReset(ht);
    return 0;
}


